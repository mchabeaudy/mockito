package mockito.example.service;

public class UnicornsInfo {

  public long totalUnicorns;

  public int maleAverageSize;

  public int femaleAverageSize;

  public long getTotalUnicorns() {
    return totalUnicorns;
  }

  public void setTotalUnicorns(long totalUnicorns) {
    this.totalUnicorns = totalUnicorns;
  }

  public int getMaleAverageSize() {
    return maleAverageSize;
  }

  public void setMaleAverageSize(int maleAverageSize) {
    this.maleAverageSize = maleAverageSize;
  }

  public int getFemaleAverageSize() {
    return femaleAverageSize;
  }

  public void setFemaleAverageSize(int femaleAverageSize) {
    this.femaleAverageSize = femaleAverageSize;
  }
}

